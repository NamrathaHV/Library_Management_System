<script>
$(function(){
  $(document).on('click', '.delete', function(e){
    e.preventDefault();
    $('#delete').modal('show');
    var id = $(this).data('id');
    getRow(id);
  });
});

function getRow(id){
  $.ajax({
    type: 'POST',
    url: 'author_row.php',
    data: {id:id},
    dataType: 'json',
    success: function(response){
      $('.aid').val(response.AUTHOR_ID);
      $('#del_author').html(response.AUTHOR_NAME);
    }
  });
}
</script>
